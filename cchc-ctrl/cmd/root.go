package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "cchc-ctrl",
	Short: "Run database management commands",
	Long: `This program runs configuration and management commands
for your database. This includes migrations for the database
as well as tasks such as removing skipped or failed jobs.

To run a management task, use one of the sub commands. For example

	cchc-ctrl ping

will test whether you have a connection to the database.`,
	// Uncomment the following line if your bare application
	// has an action associated with it:
	// Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

